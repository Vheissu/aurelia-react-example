'use strict';
var global = require('./_global'),
    $ = require('./_'),
    DESCRIPTORS = require('./_descriptors'),
    SPECIES = require('./_wks')('species');
module.exports = function(KEY) {
  var C = global[KEY];
  if (DESCRIPTORS && C && !C[SPECIES])
    $.setDesc(C, SPECIES, {
      configurable: true,
      get: function() {
        return this;
      }
    });
};
